"""
==  EQUAL
!=  NOT EQUAL
>   GREATER THAN
<   LESSER THAN
>=  GREATER THAN OR EQUAL TO
<=  LESSER THAN OR EQUAL TO
"""
a=10
b=20

print("IS IT EQUAL",a==b)
print("IS IT NOT EQUAL",a!=b)
print("IS IT GREATER THAN",a>b)
print("IS IT LESSER THAN",a<b)
print("IS IT GREATER THAN OR EQUAL TO",a>=b)
print("IS IT LESSER THAN OR EQUAL TO",a<=b)



