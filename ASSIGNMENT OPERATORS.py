"""
=   ASSIGNMENT
+=  ADDITION
-=  SUBTRACTION
*=  MULTIPLICATION
/=  DIVISION
%=  MODULUS (REMINDER OF DIVISON)
//= FLOOR DIVISION
**= EXPONENTIAL
"""
a=220
print("a =  ",a)
a+=10
print("a += ",a)
a-=10
print("a -= ",a)
a*=2
print("a *= ",a)
a/=2
print("a /= ",a)
a%=2
print("a %= ",a)
a//=2
print("a //= ",a)

b=4
b**=2
print("b **= ",b)