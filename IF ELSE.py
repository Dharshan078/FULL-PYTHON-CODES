"""
IF ELSE STATEMENT IN PYTHON
IF ELSE Statement is used for decision-making operations
It has two conditions if and else,
If the statement is true it will print "if clause" in output, if it's not true it will print "else clause" in output
EX 1: TO CHECK THE GIVEN NUMBER IS ODD OR EVEN
"""
n=int(input("ENTER A NUMBER : "))
if n%2==0:
    print(n,"IS EVEN NUMBER")
else:
    print(n,"IS ODD NUMBER")